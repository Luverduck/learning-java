package part_01.chapter_04.s04_06.t06_01;

public class RelationalOperator {
    public static void main(String[] args) {

        // 비교 연산자 (Relational Operator)
        int x = 1;
        int y = 2;
        System.out.println( "x == y : " + (x == y) );
        System.out.println( "x != y : " + (x != y) );
        System.out.println( "x > y : " + (x > y) );
        System.out.println( "x >= y : " + (x >= y) );
        System.out.println( "x < y : " + (x < y) );
        System.out.println( "x <= y : " + (x <= y) );
    }
}
