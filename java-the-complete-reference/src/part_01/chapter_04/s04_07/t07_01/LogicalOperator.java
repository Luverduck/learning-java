package part_01.chapter_04.s04_07.t07_01;

public class LogicalOperator {
    public static void main(String[] args) {

        // 논리 연산자 (Logical Operator)
        boolean x = 1 > 2;
        boolean y = 3 < 4;
        System.out.println( "x && y : " + (x && y) );
        System.out.println( "x || y : " + (x || y) );
        System.out.println( "!x : " + (!x) );

    }
}
