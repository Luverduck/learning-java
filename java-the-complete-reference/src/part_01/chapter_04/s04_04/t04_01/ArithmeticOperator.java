package part_01.chapter_04.s04_04.t04_01;

public class ArithmeticOperator {
    public static void main(String[] args) {

        // 산술 연산자(Arithmetic Operator)
        int x = 7;
        int y = 4;
        System.out.println( "x + y : " + (x + y) );
        System.out.println( "x - y : " + (x - y) );
        System.out.println( "x * y : " + (x * y) );
        System.out.println( "x / y : " + (x / y) );
        System.out.println( "x %% y : " + (x % y) );

    }
}
