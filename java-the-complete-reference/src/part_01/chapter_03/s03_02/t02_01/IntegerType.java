package part_01.chapter_03.s03_02.t02_01;

public class IntegerType {
    public static void main(String[] args) {

        // 정수형(Integer Type)

        // byte : 1[byte]
        byte iByte;
        byte iByteMin = -128;
        byte iByteMax = 127;

        // short : 2[byte]
        short iShort;
        short iShortMin = -32768;
        short iShortMax = 32767;

        // int : 4[byte]
        int iInt;
        int iIntMin = -2147483648;
        int iIntMax = 2147483647;

        // long : 8[byte]
        long iLong;
        long iLongMin = -9223372036854775808L;
        long iLongMax = 9223372036854775807L;

    }
}
