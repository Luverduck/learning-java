package part_01.chapter_03.s03_01.t01_01;

public class PrimitiveType {
    public static void main(String[] args) {

        // 기본 자료형 (Primitive Type)

        // 정수형 (Integer)
        byte iByte = 1;
        short iShort = 2;
        int iInt = 3;
        long iLong = 4L;

        // 부동소수점형 (Floating-point number)
        float fFloat = 1.0f;
        double fDouble = 1.5;

        // 논리형 (Boolean)
        boolean bBoolean = true;

        // 문자형 (Character)
        char cChar = 'A';

    }
}
