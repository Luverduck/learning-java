package part_01.chapter_03.s03_05.t05_02;

public class ExplicitTypeConversion {
    public static void main(String[] args) {

        // 명시적 형 변환 (Explicit Type Conversion)
        long iLong = (long)10;
        int iInt = (int)10L;
        double fDouble = (double)1.5f;
        float fFloat = (float)1.5;

    }
}
